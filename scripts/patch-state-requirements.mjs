// APPLY PATCHES
// ─────────────────────────────────────────────────────────────────────────────
const data = JSON.parse(readFileSync(DATA_PATH, "utf8"));
const states = data.states;
let patchCount = 0;

// Fields we write only if currently null (never overwrite intentional data)
const NULLABLE_PATCH_FIELDS = [
  "prelicense_hours_required", "prelicense_required_course",
  "exam_vendor", "exam_fee_usd", "exam_passing_score_pct",
  "exam_question_count", "exam_time_minutes",
  "fingerprint_required", "fingerprint_vendor", "fingerprint_code", "fingerprint_fee_usd",
  "license_renewal_years", "ce_hours_per_cycle", "ce_ethics_hours",
  "license_type_note", "captured_at",
];
// These fields we only set if app_fee itself is null:
const APP_FEE_FIELD = "license_application_fee_usd";
// Fingerprint required: only set if currently null
const FP_REQ_FIELD = "fingerprint_required";

function patchLine(cell, patch) {
  for (const field of NULLABLE_PATCH_FIELDS) {
    if (cell[field] == null && patch[field] !== undefined) {
      cell[field] = patch[field];
      patchCount++;
    }
  }
  // App fee — only if null
  if (cell[APP_FEE_FIELD] == null && patch[APP_FEE_FIELD] !== undefined) {
    cell[APP_FEE_FIELD] = patch[APP_FEE_FIELD];
    patchCount++;
  }
  // Fingerprint required — only if null (don't overwrite false)
  if (cell[FP_REQ_FIELD] == null && patch[FP_REQ_FIELD] !== undefined) {
    cell[FP_REQ_FIELD] = patch[FP_REQ_FIELD];
    patchCount++;
  }
}

for (const [stCode, patch] of Object.entries(STATE_LIFE)) {
  if (patch._skip) continue;
  const stRec = states[stCode];
  if (!stRec) { console.warn(`  WARN: State ${stCode} not found`); continue; }

  // Patch Life line
  if (stRec.lines?.life) {
    patchLine(stRec.lines.life, patch);
  }

  // Patch Health line — use life data as base, then apply health overrides
  if (stRec.lines?.health) {
    const healthPatch = { ...patch, ...(STATE_HEALTH_OVERRIDES[stCode] || {}) };
    patchLine(stRec.lines.health, healthPatch);
  }

  // Mortgage protection — same license as Life, so patch same exam/fp data
  if (stRec.lines?.mortgage_protection) {
    patchLine(stRec.lines.mortgage_protection, patch);
  }

  // Annuity — same exam vendor / CE / fingerprint as Life in most states
  if (stRec.lines?.annuity) {
    patchLine(stRec.lines.annuity, patch);
  }
}

writeFileSync(DATA_PATH, JSON.stringify(data, null, 2), "utf8");
console.log(`✅ Patched ${patchCount} null fields across all states.`);
console.log(`   Wrote: ${DATA_PATH}`);
